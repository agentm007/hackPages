      <h2>Description</h2>
      <p>Summation notation is used to represent the sum of a series. It is
         represented by a capital letter sigma Σ, the alt code of the character
         is 228. The parts used in a summation notation are the stopping point,
         the number or variable up top, the starting point the number or
         variable on the bottom, the index of summation, usually a character ‘i’
         that is the value iterated over and the element to be summed.</p>
