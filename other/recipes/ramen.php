      <h2>Ingredients</h2>
      <ul>
        <li>1 pack ramen noodles with flavor packet</li>
        <li>1 large egg</li>
        <li>½ teaspoon butter</li>
        <li>2 slices cheese</li>
        <li>¼ teaspoon toasted sesame seeds</li>
        <li>½ scallion, green part only, thinly sliced on the bias, optional</li>
      </ul>

      <h2>Directions</h2>
      <ol>
        <li>Bring 2 1/2 cups of water to a boil in a small saucepan. Add the
             noodles and cook for 2 minutes. Add the flavor packet, stir, and
             continue to cook for another 30 seconds</li>
        <li>Remove the pan from the heat and carefully add the egg. Do not stir;
            pull the noodles over the egg and let sit for one minute to poach.</li>
        <li>Carefully transfer everything to a serving bowl, add the butter,
            cheese and sesame seeds and mix. Garnish with the scallions if desired.</li>
      </ol>
