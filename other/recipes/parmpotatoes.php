      <h2>Ingredients</h2>
      <ul>
        <li>1/4 Cup Parmesan Chease(Green Tube)</li>
        <li>1/4 Cup Flour</li>
        <li>1/4 tsp Salt</li>
        <li>1/8 tsp Pepper</li>
        <li>1/2 Cup Butter</li>
        <li>Russet Potatoes</li>
      </ul>

      <h2>Directions</h2>
      <ol>
        <li>Peel Russet Potatoes</li>
        <li>Coat glass pan in melted butter</li>
        <li>Rince potatoes in bowl of water then throw in bag with Chease, 
            Flour, Salt and Pepper. Then shake until fully coated</li>
        <li>Place coated potatoes in pan. Sprinkle excess stuff on top</li>
        <li>put in oven on 350 for 20 min</li>
        <li>Flip and then cook for more 20 min</li>
        <li>Take out when they look crunchy</li>
      </ol>
