      <h2>Ingredients</h2>
      <ul>
        <li>2 (12 ounce) packages potato gnocchi</li>
        <li>1/4 cup butter</li>
        <li>1 clove garlic, minced</li>
        <li>1 teaspoon dried sage</li>
        <li>1/4 teaspoon salt</li>
        <li>1/4 cup grated Parmesan cheese</li>
        <li>1/4 teaspoon ground black pepper</li>
        <li>2 tablespoons grated Parmesan cheese</li>
      </ul>

      <h2>Directions</h2>
      <ol>
        <li>Bring a large pot of lightly salted water to a boil over high heat.
            Add the gnocchi pasta, and cook until they float to the surface, 2
            to 3 minutes; drain.</li>
        <li>Melt the butter in a skillet over medium heat. Stir in the garlic,
            and cook until the garlic has softened and is beginning to turn
            golden brown, about 4 minutes. Stir in the sage and salt for a few
            seconds, then add the cooked gnocchi. Toss gently with 1/4 cup of
            Parmesan cheese and the pepper. Sprinkle with the remaining 2
            tablespoons Parmesan cheese to serve.</li>
      </ol>
